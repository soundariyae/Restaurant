package com.acc.bean;

public class RfcDetailsBean {

	private String rfcId;
	private String impactedApplication;
	private String title;
	private String description;
	private String startDt;
	private String endDt;
	private String impactedNodes;
	private String requester;
	private String status;
	
	public String getRfcId() {
		return rfcId;
	}
	public void setRfcId(String rfcId) {
		this.rfcId = rfcId;
	}
	public String getImpactedApplication() {
		return impactedApplication;
	}
	public void setImpactedApplication(String impactedApplication) {
		this.impactedApplication = impactedApplication;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getStartDt() {
		return startDt;
	}
	public void setStartDt(String startDt) {
		this.startDt = startDt;
	}
	public String getEndDt() {
		return endDt;
	}
	public void setEndDt(String endDt) {
		this.endDt = endDt;
	}
	public String getImpactedNodes() {
		return impactedNodes;
	}
	public void setImpactedNodes(String impactedNodes) {
		this.impactedNodes = impactedNodes;
	}
	public String getRequester() {
		return requester;
	}
	public void setRequester(String requester) {
		this.requester = requester;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
